<?php
function skor_terbesar($arr)
{
  //kode di sini
  $arr_kelas = [];
  foreach ($arr as $row) {
    if (!isset($arr_kelas[$row['kelas']])) {
      $arr_kelas[$row['kelas']] = ['nilai' => 0];
    }
    if ($row['nilai'] > $arr_kelas[$row['kelas']]['nilai']) {
      $arr_kelas[$row['kelas']] = $row;
    }
  }
  return $arr_kelas;
}

